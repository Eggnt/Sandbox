try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Denominator must be a non-zero integer!")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
print("Finished.")

# 1. A value error will occur if a non-integer is added as numerator or denominator
# 2. A zero division error will occur if the denominator is 0
# 3. The code could specify using a while loop that the denominator must be a non-zero integer
